    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        JFileChooser jFileChooser = new JFileChooser();
        FileFilter filter = new FileNameExtensionFilter("CSV File", "csv");
        jFileChooser.addChoosableFileFilter(filter);
        int returnVal = jFileChooser.showDialog(null, "OPEN");
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = jFileChooser.getSelectedFile();
            File currentDirectory = jFileChooser.getCurrentDirectory();
            String dirName = currentDirectory.getAbsolutePath();
            String fileName = jFileChooser.getName(file);
            jTextField4.setText(dirName + "\\" + fileName);

        }
    }//GEN-LAST:event_jButton2ActionPerformed
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            FileReader in = new FileReader(jTextField4.getText());
            BufferedReader br = new BufferedReader(in);
            String line;
            while ((line = br.readLine()) != null) {
                String[] arr = line.split(",");
                String query = "INSERT INTO `student`(`St_Username`, "
                        + "`St_Firstname`, `St_Lastname`, `St_Password`, "
                        + "`St_section`, `St_Status`) VALUES "
                        + "(" + arr[0] + "," + arr[1] + "," + arr[2] + ","
                        + " 1234 ," + arr[3] + ",'first')";
                Query(query, "Inserted");
            }
            br.close();
            in.close();
            JOptionPane.showMessageDialog(this, "Success");
            dispose();
        } catch (IOException e) {

        }
    }//GEN-LAST:event_jButton1ActionPerformed

